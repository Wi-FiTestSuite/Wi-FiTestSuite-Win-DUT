void init_thr_flag()
{
    int i = 0;
    for(i=0; i< WFA_THREADS_NUM; i++)
    {
        pthread_mutex_init(&wmm_thr[i].thr_flag_mutex, NULL);
        pthread_cond_init(&wmm_thr[i].thr_flag_cond, NULL);
	pthread_mutex_init(&wmm_thr[i].thr_flag_mutex, NULL);
        pthread_cond_init(&wmm_thr[i].thr_flag_cond, NULL);
        wmm_thr[i].thr_flag = 0;
        wmm_thr[i].stop_flag = 0;
	
    }
}
